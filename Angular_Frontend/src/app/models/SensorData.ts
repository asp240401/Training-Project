/// <summary>
///     Represents data points to be plotted on the chart/graph.
/// </summary>
export class SensorData
{
    label:any       // Represents the label or timestamp for the data point
    y:any           // Represents the sensor data value
    color:any       // Represents the color of the data point in the chart
    lineColor:any   // Represents the line color for the data point in the chart
    x:any           // Represents the x-axis value for the data point in the chart
}